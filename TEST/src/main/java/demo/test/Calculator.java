    private void equalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_equalActionPerformed
        // TODO add your handling code here:
        try {
            Set<String> op = new HashSet<>();
            op.add("AND"); op.add("XOR"); op.add("OR"); op.add(">>"); op.add("<<"); op.add("mod"); op.add("gcd");
            op.add("modinv"); op.add("lcm");
            if (op.contains(operator)) {
                second = Long.parseLong(field.getText());
                jLabel1.setText(jLabel1.getText()+field.getText());
                if (operator == "gcd" || operator == "lcm") jLabel1.setText(jLabel1.getText()+")");
            }
            else if (operator != "%" && operator != "!") {
                se = Double.parseDouble(field.getText());
                jLabel1.setText(jLabel1.getText()+field.getText());
            }
            switch(operator) {
                case "+":
                    result = fi+se;
                    break;
                case "-":
                    result = fi-se;
                    break;
                case "*":
                    result = fi*se;
                    break;
                case "/":
                    try {
                        result = fi/se;
                    } catch(Exception e) {
                        System.out.println(e);
                    } break;
                case "%":
                    result = fi/100;
                    break;
                case "!":
                    res = factorial(val);
                    break;
                case "AND":
                    res = first&second;
                    break;
                case "OR":
                    res = first|second;
                    break;
                case "XOR":
                    res = first^second;
                    break;
                case ">>":
                    res = first >> second;
                    break;
                case "<<":
                    res = first << second;
                    break;
                case "mod":
                    try {
                        res = first%second;
                    } catch(Exception e) {
                        System.out.println(e);
                    } break;
                case "gcd":
                    res = gCd(first, second);
                    break;
                case "lcm":
                    res = lCm(first, second);
                    break;
                case "modinv":
                    res = modinv(first, second);
                    System.out.print(res);
                    if (res == 1) {
                         x = (x % second + second) % second;
                         inv = x;
                    } else inv = -1;
                    break;
                case "x^y":
                    result = Math.pow(fi, se);
                    break;
            } op.add("!");
            if (op.contains(operator)) {
                if (operator == "modinv") {
                    if (inv == -1) field.setText("NO SOLUTION");
                    else field.setText(Long.toString(inv));
                    return;
                }
                ans = Long.toString(res);
                field.setText(ans);
            } else {
                ans = String.format("%.5f", result);
                field.setText(ans);
            }
        } catch(Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_equalActionPerformed

