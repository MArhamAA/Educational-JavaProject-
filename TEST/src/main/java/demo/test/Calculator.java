    private long factorial(long val) {
        Long value = 1L;
        while (val > 0) {
            value *= val; val--;
        } return value;
    }

    private long gCd(long first, long second) {
        if (second == 0) return first;
        return gCd(second, first%second);
    }

    private long modinv(long first, long second) {
        return eua(first, second);
    }
    
    private long eua(long first, long second) {
        x = 1L; y = 0L;
        Long x1 = 0L, y1 = 1L, a1 = first, b1 = second;
        while (b1 != 0) {
            Long q = a1 / b1;
            Long temp;
            temp = x1;
            x1 = x-q*x1; x = temp;
            temp = y1;
            y1 = y-q*y1; y = temp;
            temp = b1;
            b1 = a1 - q*b1; a1 = temp;
        }
        return a1;
    }

    private long lCm(long first, long second) {
        return first/gCd(first, second)*second;
    }
    
    
